%% MATLAB as a calculator

% copy/paste the code into the command window to evaluate
1+1

% spacing can improve code readability
4*5+8
4 * 5 + 8
4*5 + 8

% parentheses also improve code readability
5*3/6^2
(5*3) / (6^2)

% parentheses can also override order-of-operations
3^2.3+4.1
3^(2.3+4.1)



%% The colon operator

% basic use
1:5
7:109
-15:4

% non-default skipping
1:.5:5

% count backwards
10:1

% MATLAB stays within the boundaries
1:5:10



